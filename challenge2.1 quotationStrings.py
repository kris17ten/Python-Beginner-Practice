#Quotation manipulation
#Demonstrates string methods

#quote from Walt Disney

quote = "It's kinda fun to do the impossible."

print("Original Quote:")
print(quote)

print("\nIn uppercase:")
print(quote.upper())

print("\nIn lowercase:")
print(quote.lower())

print("\nAs a title:")
print(quote.title())

print("\nWith minor replacement:")
print(quote.replace("impossible", "possible"))

print("\nOriginal quote is still:")
print(quote)

input("\n\nPress the enter key to exit.")
